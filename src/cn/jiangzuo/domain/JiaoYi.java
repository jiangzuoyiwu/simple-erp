package cn.jiangzuo.domain;

import java.sql.Date;

public class JiaoYi {
	private int id;
	private String kehu;
	private String sefen_id;
	private Date riqi;
	private String yanse;
	private float jiage;
	private String pantone;
	private String yuanliao;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getKehu() {
		return kehu;
	}
	public void setKehu(String kehu) {
		this.kehu = kehu;
	}
	public String getSefen_id() {
		return sefen_id;
	}
	public void setSefen_id(String sefen_id) {
		this.sefen_id = sefen_id;
	}
	public Date getRiqi() {
		return riqi;
	}
	public void setRiqi(Date riqi) {
		this.riqi = riqi;
	}
	public String getYanse() {
		return yanse;
	}
	public void setYanse(String yanse) {
		this.yanse = yanse;
	}
	public float getJiage() {
		return jiage;
	}
	public void setJiage(float jiage) {
		this.jiage = jiage;
	}
	public String getPantone() {
		return pantone;
	}
	public void setPantone(String pantone) {
		this.pantone = pantone;
	}
	public String getYuanliao() {
		return yuanliao;
	}
	public void setYuanliao(String yuanliao) {
		this.yuanliao = yuanliao;
	}
	

}
